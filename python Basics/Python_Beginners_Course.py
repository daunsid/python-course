## Module 3: Control Structures
# Code Sample
number = 5
if number > 0:
    print("Positive")
elif number < 0:
    print("Negative")
else:
    print("Zero")

for i in range(5):
    print(i)

count = 0
while count < 5:
    print(count)
    count += 1

